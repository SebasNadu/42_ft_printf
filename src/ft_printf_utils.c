int	ft_isflag(char c)
{
	if (c == '-' || c == '0' || c == '.' || c == '#'
		|| c == ' ' || c == '+')
		return (1);
	return (0);
}

int	ft_isspec(char c)
{
	if (c == 's' || c == 'c' || c == 'p' || c == 'd' || c == 'i'
		|| c == 'u' || c == 'x' || c == 'X' || c == '%')
		return (1);
	return (0);
}
